// this file holds the math functions

add = function addition(var1,var2){
    result = var1 + var2
    return result
}

sub = function subtraction(var1,var2){
    result = var1 - var2
    return result
}

mul = function multiplication(var1,var2){
    result = var1 * var2
    return result
}

div = function division(var1,var2){
    result = var1 / var2
    return result
}

module.exports.add = add